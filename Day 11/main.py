'''
> - Greater than
< - Less than
>= - Greater than or equal to
<= - Less than or equal to
== - Equal to
!= - Not equal to
'''

a = int(input("Enter your age: "))
print("Your age is", a, "years old.")
print(a>18)
print(a<=18)
print(a==18)
print(a!=18)
print(a>=18)    
if (a >18):
    print("You are an adult")
else:
    print("You are a child")

